    private void ingresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarBtnActionPerformed
        String url = "jdbc:postgresql://localhost:5432/sic115";
        String usuario = usuarioTxt.getText();
        String contrasena = new String(contrasenaTxt.getPassword());
        
        try{
            conn = DriverManager.getConnection(url, usuario, contrasena);
            this.dispose();
            new Dashboard(conn).setVisible(true);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Credenciales incorrectas", "Error de Inicio de Sesi√≥n", JOptionPane.ERROR_MESSAGE);
            mostrarErrores(true);
        }
    }//GEN-LAST:event_ingresarBtnActionPerformed

